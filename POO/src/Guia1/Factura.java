package Guia1;

public class Factura {
	
	private String numDePieza;
	private String descPieza;
	private int cantArticulo;
	private double priceArticulo;
	
	
	public String getNumDePieza() {
		return numDePieza;
	}
	public void setNumDePieza(String numDePieza) {
		this.numDePieza = numDePieza;
	}
	public String getDescPieza() {
		return descPieza;
	}
	public void setDescPieza(String descPieza) {
		this.descPieza = descPieza;
	}
	public int getCantArticulo() {
		return cantArticulo;
	}
	public void setCantArticulo(int cantArticulo) {
		this.cantArticulo = cantArticulo;
	}
	public double getPriceArticulo() {
		return priceArticulo;
	}
	public void setPriceArticulo(double priceArticulo) {
		this.priceArticulo = priceArticulo;
	}
	
	

}
